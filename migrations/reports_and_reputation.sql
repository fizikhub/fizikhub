-- ==========================================
-- REPUTATION FUNCTIONS
-- ==========================================

-- Function to add reputation
CREATE OR REPLACE FUNCTION add_reputation(
    p_user_id UUID,
    p_points INTEGER,
    p_reason TEXT,
    p_reference_type TEXT DEFAULT NULL,
    p_reference_id BIGINT DEFAULT NULL
)
RETURNS void AS $$
BEGIN
    -- Update user's total reputation
    UPDATE profiles 
    SET reputation = reputation + p_points 
    WHERE id = p_user_id;
    
    -- Record in history
    INSERT INTO reputation_history (user_id, points, reason, reference_type, reference_id)
    VALUES (p_user_id, p_points, p_reason, p_reference_type, p_reference_id);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- ==========================================
-- TRIGGERS FOR AUTOMATIC REPUTATION
-- ==========================================

-- When answer is accepted
CREATE OR REPLACE FUNCTION award_reputation_for_accepted_answer()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.is_accepted = true AND (OLD.is_accepted IS NULL OR OLD.is_accepted = false) THEN
        PERFORM add_reputation(NEW.author_id, 15, 'answer_accepted', 'answer', NEW.id);
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trigger_accepted_answer_reputation ON answers;
CREATE TRIGGER trigger_accepted_answer_reputation
    AFTER UPDATE ON answers
    FOR EACH ROW
    EXECUTE FUNCTION award_reputation_for_accepted_answer();

-- ==========================================
-- INDEXES
-- ==========================================

CREATE INDEX IF NOT EXISTS idx_reports_status ON reports(status);
CREATE INDEX IF NOT EXISTS idx_reports_content ON reports(content_type, content_id);
CREATE INDEX IF NOT EXISTS idx_reputation_history_user ON reputation_history(user_id);
CREATE INDEX IF NOT EXISTS idx_profiles_reputation ON profiles(reputation DESC);

