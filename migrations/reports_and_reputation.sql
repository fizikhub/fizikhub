-- Report/Flag System & Reputation System
-- Content moderation and user gamification

-- ==========================================
-- REPORTS TABLE (Flag/Report System)
-- ==========================================

CREATE TABLE IF NOT EXISTS reports (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    reporter_id UUID REFERENCES profiles(id) ON DELETE SET NULL,
    content_type TEXT NOT NULL, -- 'question', 'answer', 'article', 'comment'
    content_id BIGINT NOT NULL,
    reason TEXT NOT NULL, -- 'spam', 'offensive', 'misinformation', 'duplicate', 'other'
    description TEXT,
    status TEXT NOT NULL DEFAULT 'pending', -- 'pending', 'reviewed', 'resolved', 'dismissed'
    reviewed_by UUID REFERENCES profiles(id) ON DELETE SET NULL,
    reviewed_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- ==========================================
-- REPUTATION SYSTEM
-- ==========================================

-- Add reputation column to profiles if not exists
ALTER TABLE profiles ADD COLUMN IF NOT EXISTS reputation INTEGER DEFAULT 0;

-- Reputation history table
CREATE TABLE IF NOT EXISTS reputation_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES profiles(id) ON DELETE CASCADE NOT NULL,
    points INTEGER NOT NULL, -- Can be positive or negative
    reason TEXT NOT NULL, -- 'question_upvote', 'answer_accepted', 'answer_upvote', etc.
    reference_type TEXT, -- 'question', 'answer', 'article'
    reference_id BIGINT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

